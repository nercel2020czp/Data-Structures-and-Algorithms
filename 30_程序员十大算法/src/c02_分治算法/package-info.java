/*14.2 分治算法
14.2.1 分治算法介绍
1) 分治法是一种很重要的算法。 字面上的解释是“分而治之” ， 就是把一个复杂的问题分成两个或更多的相同或
相似的子问题， 再把子问题分成更小的子问题……直到最后子问题可以简单的直接求解， 原问题的解即子问题
的解的合并。 这个技巧是很多高效算法的基础， 如排序算法(快速排序， 归并排序)， 傅立叶变换(快速傅立叶变
换)……
2) 分治算法可以求解的一些经典问题
 二分搜索
 大整数乘法
 棋盘覆盖
 合并排序
 快速排序
 线性时间选择
 最接近点对问题
 循环赛日程表
 汉诺塔

3) 合并： 将各个子问题的解合并为原问题的解。
*/